    public void wyszukaj() {
        String dr1 = (String) cbDruzyna1.getSelectedItem();
        String dr2 = (String) cbDruzyna2.getSelectedItem();
        Object[][] tableaBody = MeczK.getInstance().getMecze(dr1, dr2, null, null, null, false, false);
        String[] tablaHead = {"#", "Gospodarze", "Goście", "Data", "Wyniki", "ID"};
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                tableaBody,
                tablaHead
        ));
        jTable1.getColumnModel().getColumn(5).setMinWidth(0);
        jTable1.getColumnModel().getColumn(5).setMaxWidth(0);
    }

    private void initPanelBoczny() {

        List<String> dzien = new ArrayList<String>();
        for (int i = 1; i < 32; i++) {
            dzien.add("" + i);
        }
        List<String> miesiac = new ArrayList<String>();
        for (int i = 1; i < 13; i++) {
            miesiac.add("" + i);
        }
        int year = Calendar.getInstance().get(Calendar.YEAR);

        List<String> rok = new ArrayList<String>();
        for (int i = 0; i < 121; i++) {
            int rok2 = year - i;
            rok.add("" + rok2);
        }

        cbDzienDo.setModel(new DefaultComboBoxModel(dzien.toArray()));
        cbDzienOd.setModel(new DefaultComboBoxModel(dzien.toArray()));

        cbMiesiacDo.setModel(new DefaultComboBoxModel(miesiac.toArray()));
        cbMiesiacOd.setModel(new DefaultComboBoxModel(miesiac.toArray()));

        cbRokDo.setModel(new DefaultComboBoxModel(rok.toArray()));
        cbRokOd.setModel(new DefaultComboBoxModel(rok.toArray()));

        cbSort.setModel(new javax.swing.DefaultComboBoxModel(new String[]{"Gospodarze", "Goście", "Data"}));

        sortMap.put("Gospodarze", "gosp");
        sortMap.put("Goście", "gosc");
        sortMap.put("Data", "data");
    }

