    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int dodajCol = 4;
        if (jTable1.columnAtPoint(evt.getPoint()) == dodajCol) {
            int id_meczu = (int) jTable1.getValueAt(jTable1.rowAtPoint(evt.getPoint()), 5);
            if ((boolean) jTable1.getValueAt(jTable1.rowAtPoint(evt.getPoint()), 4)) {

                WprowadzWynik wprowadzWynik = new WprowadzWynik(null, true, id_meczu, (String) jTable1.getValueAt(jTable1.rowAtPoint(evt.getPoint()), 0), (String) jTable1.getValueAt(jTable1.rowAtPoint(evt.getPoint()), 1));
                wprowadzWynik.setVisible(true);
                if (wprowadzWynik.idAdded()) {
                    jTable1.setValueAt(true, jTable1.rowAtPoint(evt.getPoint()), jTable1.columnAtPoint(evt.getPoint()));
                } else {
                    jTable1.setValueAt(false, jTable1.rowAtPoint(evt.getPoint()), jTable1.columnAtPoint(evt.getPoint()));
                }
            } else {
                Zaklad.getInstance().deleteZakladSkladowy(new ZakladSkladowy(id_meczu, 0, (short)0, (short)0));
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked


