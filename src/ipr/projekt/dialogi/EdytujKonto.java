    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int dialogResult = JOptionPane.showConfirmDialog(null, "Czy na pewno chcesz usunąć konto?", "Uwaga!", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            MySql mySQL = MySql.getInstance();
            Connection connection = mySQL.getConnection();
            PreparedStatement preparedStatement;
            try {
                preparedStatement = connection.prepareStatement("DELETE FROM klienci WHERE id_klienta = ?");
                preparedStatement.setLong(1, Klient.getInstance().getId());
                int ile = preparedStatement.executeUpdate();
                if (ile == 1) {
                    returnCode = 2;
                    dispose();
                } else {
                    lblBledy.setText("Brak użytkownika w bazie");
                }
            } catch (SQLException ex) {
                Logger.getLogger(LoginDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

