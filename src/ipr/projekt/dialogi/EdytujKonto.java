    private void initDane() {
        Klient klient = Klient.getInstance();
        fieldImie.setText(klient.getImie());
        fieldNazwisko.setText(klient.getNazwisko());
        fieldAdres1.setText(klient.getAdres());
        fieldKod.setText(klient.getKod());
        fieldMiasto.setText(klient.getMiasto());
        java.util.Date urData = klient.getDate();
        
        cbDzien.setSelectedItem("" + urData.getDate());
        cbMiesiac.setSelectedItem("" + urData.getMonth());
        int rok = urData.getYear() + 1900;
        cbRok.setSelectedItem("" +  rok);
    }
